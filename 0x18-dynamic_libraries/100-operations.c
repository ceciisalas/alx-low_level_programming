/**
 * add -sum
 * @a: number
 * @b: number
 * Return: Result
 */

int add(int a, int b)
{
return (a + b);
}

/**
 * sub - substraction
 * @a: number
 * @b: number
 * Return: Result
 */

int sub(int a, int b)
{
return (a - b);
}

/**
 * mul - multiplication
 * @a: number
 * @b: number
 * Return: Result
 */

int mul(int a, int b)
{
return (a * b);
}

/**
 * div - division
 * @a: number
 * @b: number
 * Return: Result
 */

int div(int a, int b)
{
return (a / b);
}

/**
 * mod - modulo
 * @a: number
 * @b: number
 * Return: Result
 */

int mod(int a, int b)
{
return (a % b);
}
